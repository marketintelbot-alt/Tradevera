import { writeFileSync } from "node:fs";
import { resolve } from "node:path";

const lastUpdatedIso = new Date().toISOString();
const outputPath = resolve(process.cwd(), "src", "generated", "buildMeta.ts");
const content = `// Auto-generated by scripts/generate-build-meta.mjs\nexport const BUILD_LAST_UPDATED_ISO = ${JSON.stringify(lastUpdatedIso)};\n`;

writeFileSync(outputPath, content, "utf8");
console.log(`Wrote build metadata: ${lastUpdatedIso}`);
